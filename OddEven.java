public class OddEven {
    public static void main(String[] args) {
        // to print the number is even or odd

        for(int i = 0 ; i <= 10 ; i++){
            if( i%2 != 0){
                System.out.println(i + " is a odd number");
            }
            else{
                System.out.println(i + " is a even number");
            }
        }
    }    
}
